package Encapsulation;

public class Insta {
	private long mobno;
	private String password;
	
	public void setmobno(long mobno)
	{
		this.mobno = mobno;
	}
	public long getmobno()
	{
		return mobno;
	}
	
	public void setpassword(String password)
	{
		this.password = password;
	}
	public String getpassword()
	{
		return password;
	}
}
